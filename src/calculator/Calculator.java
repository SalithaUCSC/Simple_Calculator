    public void artithmeticOperation (){
        switch(calculation){
            //addition
            case 1:
                ans = num + Double.parseDouble(numField.getText());
                numField.setText(Double.toString(ans));
                break;
            //substraction
            case 2:
                ans = num - Double.parseDouble(numField.getText());
                numField.setText(Double.toString(ans));
                break;
            //multiplication
            case 3:
                ans = num * Double.parseDouble(numField.getText());
                numField.setText(Double.toString(ans));
                break;
            // division
            case 4:
                ans = num / Double.parseDouble(numField.getText());
                numField.setText(Double.toString(ans));
                break;
        }
    }

