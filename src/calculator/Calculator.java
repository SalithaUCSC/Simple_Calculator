    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        int length = numField.getText().length();
        int number = numField.getText().length() - 1;
        String store;
        
        if (length > 0) {
            StringBuilder back = new StringBuilder(numField.getText());
            back.deleteCharAt(number);
            store = back.toString();
            numField.setText(store);
        }
    }//GEN-LAST:event_btnBackActionPerformed

